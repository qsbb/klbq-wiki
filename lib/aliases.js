/**
 * 将内置别名表转换为大小写不敏感的 Map
 * @param {string} customAliasesText 自定义别名文本，每行一条 "别名=页面标题"
 * @returns {Map<string, string>} 别名 -> 页面标题
 */
export function buildAliasMap(customAliasesText = '') {
  const map = new Map()
  for (const [alias, title] of Object.entries(BUILTIN_ALIASES)) {
    map.set(alias.toLowerCase(), title)
  }
  const text = (customAliasesText || '').trim()
  if (text) {
    for (const line of text.split(/\r?\n/)) {
      if (!line.includes('=')) continue
      const idx = line.indexOf('=')
      const alias = line.slice(0, idx).trim()
      const title = line.slice(idx + 1).trim()
      if (alias && title) {
        map.set(alias.toLowerCase(), title)
      }
    }
  }
  return map
}
